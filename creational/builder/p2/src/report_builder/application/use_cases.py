"""Directors and use cases for Report Builder."""
from __future__ import annotations

from report_builder.domain.entities import ChartSpec, DataTable, Report
from report_builder.domain.interfaces import ReportBuilder


class SalesReportDirector:
    """Knows how to build a sales report — agnostic of output format.

    DIP: depends on ReportBuilder abstraction, not on PDF/Excel/CSV.
    """

    def __init__(self, builder: ReportBuilder) -> None:
        self._builder = builder

    def build(self, data: dict[str, object]) -> Report:
        period: str = str(data.get("period", ""))
        rows: list[list[str | int | float]] = data.get("rows", [])  # type: ignore[assignment]
        total: float = float(data.get("total", 0.0))

        table = DataTable(
            title="Sales Detail",
            headers=["Date", "Customer", "Product", "Qty", "Amount"],
            rows=rows,
        )
        chart = ChartSpec(
            chart_type="bar",
            title="Sales by Date",
            x_labels=[str(r[0]) for r in rows],
            y_values=[float(r[4]) if len(r) > 4 else 0.0 for r in rows],
        )

        return (
            self._builder
            .set_title(f"Sales Report — {period}")
            .add_header(f"Period: {period}")
            .add_data_table(table)
            .add_chart(chart)
            .add_footer(f"Total: {total:.2f} | Generated by Sales Report Director")
            .build()
        )


class InventoryReportDirector:
    """Knows how to build an inventory report — agnostic of output format."""

    def __init__(self, builder: ReportBuilder) -> None:
        self._builder = builder

    def build(self, data: dict[str, object]) -> Report:
        warehouse: str = str(data.get("warehouse", "Main"))
        rows: list[list[str | int | float]] = data.get("rows", [])  # type: ignore[assignment]

        table = DataTable(
            title="Stock Levels",
            headers=["SKU", "Product", "Category", "Stock", "Reorder Level"],
            rows=rows,
        )

        return (
            self._builder
            .set_title(f"Inventory Report — {warehouse}")
            .add_header(f"Warehouse: {warehouse}")
            .add_data_table(table)
            .add_footer("Generated by Inventory Report Director")
            .build()
        )


class GenerateReportUseCase:
    """Delegates report generation to the appropriate director."""

    def __init__(self, builder: ReportBuilder) -> None:
        self._builder = builder

    def execute_sales(self, data: dict[str, object]) -> Report:
        return SalesReportDirector(self._builder).build(data)

    def execute_inventory(self, data: dict[str, object]) -> Report:
        return InventoryReportDirector(self._builder).build(data)
