"""Streamlit application — UI Component Decorator pattern showcase.

Lets the user pick decorators interactively and stacks them live on a
base widget, rendering the resulting HTML so the effect of each layer
is visible immediately.
"""

from __future__ import annotations

import streamlit as st

from ui_components.domain.entities import WidgetSpec
from ui_components.domain.interfaces import UIComponent
from ui_components.infrastructure.decorators import (
    BadgeDecorator,
    BorderDecorator,
    CollapsibleDecorator,
    ShadowDecorator,
    TooltipDecorator,
)
from ui_components.infrastructure.widgets import TextWidget

st.set_page_config(page_title="UI Component Decorator", page_icon="🧩")


def _build_widget_from_selection(
    spec: WidgetSpec,
    selected_decorators: list[str],
    border_color: str,
    badge_text: str,
    tooltip_text: str,
    collapsible_summary: str,
) -> UIComponent:
    """Stack decorators in the order the user selected them."""
    widget: UIComponent = TextWidget(spec)
    for decorator_name in selected_decorators:
        if decorator_name == "Border":
            widget = BorderDecorator(widget, color=border_color)
        elif decorator_name == "Shadow":
            widget = ShadowDecorator(widget)
        elif decorator_name == "Tooltip":
            widget = TooltipDecorator(widget, tooltip_text)
        elif decorator_name == "Badge":
            widget = BadgeDecorator(widget, badge_text)
        elif decorator_name == "Collapsible":
            widget = CollapsibleDecorator(widget, collapsible_summary)
    return widget


def render_app() -> None:
    """Render the Streamlit page: controls on the sidebar, preview on main."""
    st.title("UI Component Decorator")
    st.caption("GoF Decorator pattern applied to Streamlit widgets")

    with st.sidebar:
        st.header("Compose your widget")
        available = ["Border", "Shadow", "Tooltip", "Badge", "Collapsible"]
        selected = st.multiselect(
            "Decorators (applied in order)", available, ["Border"]
        )
        border_color = st.color_picker("Border color", "#4B8BBE")
        badge_text = st.text_input("Badge text", "NEW")
        tooltip_text = st.text_input("Tooltip text", "Generated by a decorator stack")
        collapsible_summary = st.text_input("Collapsible summary", "Show details")

    spec = WidgetSpec(
        widget_id="demo-widget",
        label="Sales Summary",
        content="Q2 revenue grew 12% quarter over quarter.",
    )
    widget = _build_widget_from_selection(
        spec, selected, border_color, badge_text, tooltip_text, collapsible_summary
    )

    st.subheader("Rendered widget")
    st.markdown(widget.render(), unsafe_allow_html=True)

    st.subheader("Decoration chain")
    st.code(widget.describe())


if __name__ == "__main__":
    render_app()
